class Destination {
  final String id;
  final String name;
  final String location;
  final String description;
  final double price;
  final double rating;
  final String image;
  final String category;
  final int days;

  const Destination({
    required this.id,
    required this.name,
    required this.location,
    required this.description,
    required this.price,
    required this.rating,
    required this.image,
    required this.category,
    required this.days,
  });
}